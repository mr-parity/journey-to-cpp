/*
 * C++ Program: Hello World
 *
 * 1. #include <iostream>
 *    - Preprocessor directive that includes the iostream library.
 *    - Provides standard input and output facilities such as
 *      std::cout and std::cin.
 *
 * 2. int main()
 *    - The entry point of every C++ program.
 *    - Program execution begins from this function.
 *
 * 3. std::cout
 *    - Standard output stream used to display data on the console.
 *
 * 4. <<
 *    - Stream insertion operator.
 *    - Sends data to the output stream for display.
 *
 * 5. "Hello, World!"
 *    - String literal containing the text to be printed.
 *
 * 6. std::endl
 *    - Inserts a newline character and flushes the output buffer.
 *
 * 7. return 0;
 *    - Terminates the program and returns a success status
 *      to the operating system.
 */

#include<iostream>

int main() {
    std::cout<<"Hello, World!"<<std::endl;
    return 0;
}